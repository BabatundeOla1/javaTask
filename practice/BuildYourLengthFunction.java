public class BuildYourLengthFunction{

   public static void main(String [] args){
	
	int [] numbers = {3, 2, 4, 6, 9, 12, 5, 7, 15, 1};
	
	getLength(numbers);
	
   }


   public static void getLength(int [] array){

	//int counter = 0;
	int count = 0;
	while(count < array)
		count++;
		System.out.print(count);

   }














}